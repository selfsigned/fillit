#include "libft.h"

int	ft_cntword(const char *s, char c)
{
	int res;

	if (!*s || !c)
		return ((!c && *s) ? 1 : 0);
	res = (*s == c) ? 0 : 1;
	while (*(s + 1))
	{
		if (*s == c && *(s + 1) != c)
			res += 1;
		s++;
	}
	return (res);
}
