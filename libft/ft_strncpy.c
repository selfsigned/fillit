#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;

	i = -1;
	while (++i < len)
		if (i < ft_strlen(src))
			dst[i] = src[i];
		else
			dst[i] = '\0';
	return (dst);
}
